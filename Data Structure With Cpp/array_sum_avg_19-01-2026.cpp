#include <iostream>
using namespace std;

int main() {

    int a[5], b[5], c[5], sum = 0;
    
    for(int i = 0; i < 5; i++){
        cin >> a[5]>> b[5]>> c[5];
        sum = a[5] + b[5] + c[5];
    }

    float average = sum / 5;

    cout << "Average of the Numbers: " << average << endl;
    
    return 0;
}